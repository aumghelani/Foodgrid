# ─────────────────────────────────────────────────────────────────────────────
# Seed runner
# ─────────────────────────────────────────────────────────────────────────────

def seed():
    print("=== FoodGrid Boston — seed_mock_data ===\n")

    # ── Tracts ────────────────────────────────────────────────────────────────
    tracts = build_tracts_from_geojson()
    print(f"Loaded {len(tracts)} tracts from {GEOJSON_PATH.name}.")

    col_t = tracts_col()
    # Clear stale tracts so old FIPS codes from previous seeds don't accumulate
    deleted = col_t.delete_many({}).deleted_count
    if deleted:
        print(f"  Cleared {deleted} stale tracts.")
    t_upserted = 0
    for tract in tracts:
        r = col_t.update_one(
            {"tract_id": tract["tract_id"]},
            {"$set": tract},
            upsert=True,
        )
        if r.upserted_id or r.modified_count:
            t_upserted += 1
    print(f"Tracts   : {t_upserted}/{len(tracts)} upserted.")

    # ── Resources ─────────────────────────────────────────────────────────────
    col_r = resources_col()
    r_upserted = 0
    for resource in RESOURCES:
        r = col_r.update_one(
            {"resource_id": resource["resource_id"]},
            {"$set": resource},
            upsert=True,
        )
        if r.upserted_id or r.modified_count:
            r_upserted += 1
    print(f"Resources: {r_upserted}/{len(RESOURCES)} upserted.")

    # ── City stats ────────────────────────────────────────────────────────────
    # Compute from the loaded tract data for accuracy
    scores = [t["food_risk_score"] for t in tracts]
    avg_equity = round(sum(1 - s for s in scores) / len(scores), 3)
    avg_transit = round(sum(t["transit_coverage"] for t in tracts) / len(tracts), 3)
    high_risk   = sum(1 for s in scores if s >= 0.65)

    city_stats = {
        "_id":             "singleton",
        "_type":           "city_stats",
        "equity_score":    avg_equity,
        "transit_coverage": avg_transit,
        "high_risk_tracts": high_risk,
        "total_tracts":    len(tracts),
    }
    city_stats_col().update_one(
        {"_id": "singleton"},
        {"$set": city_stats},
        upsert=True,
    )
    print(f"City stats: updated (total_tracts={len(tracts)}, high_risk={high_risk}).")

    print("\nDone. MongoDB collections are ready for development.")


if __name__ == "__main__":
    seed()
