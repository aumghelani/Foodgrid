DEFAULT_POPULATION = 3500


def _deterministic_risk(geoid: str) -> float:
    """Mirror of deterministicRisk() in censusTracts.ts."""
    n   = int(geoid[-5:]) if geoid[-5:].isdigit() else 0
    mix = (n * 1619 + 37 * (n % 97) + 11) % 100
    if mix < 20:  return round(0.15 + (mix / 20) * 0.15, 4)
    if mix < 55:  return round(0.30 + ((mix - 20) / 35) * 0.20, 4)
    if mix < 85:  return round(0.50 + ((mix - 55) / 30) * 0.20, 4)
    return              round(0.70 + ((mix - 85) / 15) * 0.18, 4)


def _default_scores(geoid: str, namelsad: str) -> dict:
    risk = _deterministic_risk(geoid)
    return dict(
        tract_name=namelsad,
        food_risk_score=risk,
        equity_score=round(0.95 - risk, 2),
        transit_coverage=round(0.55 + (1 - risk) * 0.4, 2),
        food_insecurity_rate=round(risk * 0.38, 2),
        poverty_rate=round(risk * 0.44, 2),
        snap_rate=round(risk * 0.50, 2),
        population=DEFAULT_POPULATION,
        vulnerability_index=round(risk * 0.92, 2),
        need_score=round(risk * 0.95, 2),
        supply_score=round(0.90 - risk * 0.75, 2),
    )


def build_tracts_from_geojson() -> list:
    """Read boston_tracts.geojson and return a list of tract dicts for MongoDB."""
    if not GEOJSON_PATH.exists():
        print(f"ERROR: {GEOJSON_PATH} not found.")
        print("  Run: python scripts/convert_shapefile.py")
        sys.exit(1)

    with GEOJSON_PATH.open(encoding="utf-8") as f:
        geojson = json.load(f)

    tracts = []
    for feature in geojson["features"]:
        props   = feature.get("properties", {})
        geoid   = props.get("GEOID", "")
        namelsad = props.get("NAMELSAD", f"Tract {props.get('TRACTCE','')}")

        if geoid in TRACT_RISK_SCORES:
            scores = TRACT_RISK_SCORES[geoid]
        else:
            scores = _default_scores(geoid, namelsad)

        tractce = props.get("TRACTCE", "000000")
        numeric_id = int(tractce) if tractce.isdigit() else 0

        tracts.append({
            "tract_id":   geoid,
            "numeric_id": numeric_id,
            "geometry":   feature["geometry"],
            **scores,
        })

    return tracts


